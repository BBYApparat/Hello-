Shared.DumpTable = function(table,nb)
	if nb == nil then
		nb = 0
	end
	if type(table) == 'table' then
		local s = ''
		for _ = 1,nb + 1,1 do
			s = s .. "    "
		end
		s = '{\n'
		for k,v in pairs(table) do
			if type(k) ~= 'number' then k = '"'..k..'"' end
			for _ = 1,nb,1 do
				s = s .. "    "
			end
			s = s .. '['..k..'] = ' .. Shared.DumpTable(v,nb + 1) .. ',\n'
		end
		for i = 1,nb,1 do
			s = s .. "    "
		end
		return s .. '}'
	else
		return tostring(table)
	end
end

Shared.RandomInt = function(length)
	if length > 0 then
		return Shared.RandomInt(length-1) .. NumberCharset[math.random(1,#NumberCharset)]
	else
		return ''
	end
end

Shared.SplitStr = function(str,delimiter)
	local result = {}
	local from  = 1
	local delim_from,delim_to = string.find( str,delimiter,from  )
	while delim_from do
		table.insert( result,string.sub( str,from ,delim_from-1 ) )
		from  = delim_to + 1
		delim_from,delim_to = string.find( str,delimiter,from  )
	end
	table.insert( result,string.sub( str,from  ) )
	return result
end

